-- | The @__int128@ support runtime (src/main.c).
--
-- @unsigned __int128@ is desugared by the parser into a two-u64 struct, and
-- sema rewrites its operators into calls to the helpers below. When any
-- translation unit mentions __int128, the driver compiles this source as one
-- more translation unit, which supplies them.
module C99.Runtime
  ( u128RuntimeSrc
  ) where

u128RuntimeSrc :: String
u128RuntimeSrc =
  unlines
    [ "typedef struct __c99m_u128 { unsigned long long lo; unsigned long long hi; } __c99m_u128;"
    , "__c99m_u128 __c99m_u128_from_u64(unsigned long long x) {"
    , "  __c99m_u128 r; r.lo = x; r.hi = 0; return r;"
    , "}"
    , "unsigned long long __c99m_u128_to_u64(__c99m_u128 a) { return a.lo; }"
    , "__c99m_u128 __c99m_u128_add(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo + b.lo;"
    , "  r.hi = a.hi + b.hi + (r.lo < a.lo ? 1ULL : 0ULL); return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_sub(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo - b.lo;"
    , "  r.hi = a.hi - b.hi - (a.lo < b.lo ? 1ULL : 0ULL); return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_neg(__c99m_u128 a) {"
    , "  __c99m_u128 z; z.lo = 0; z.hi = 0; return __c99m_u128_sub(z, a);"
    , "}"
    , "__c99m_u128 __c99m_u128_not(__c99m_u128 a) {"
    , "  __c99m_u128 r; r.lo = ~a.lo; r.hi = ~a.hi; return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_and(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo & b.lo; r.hi = a.hi & b.hi; return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_or(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo | b.lo; r.hi = a.hi | b.hi; return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_xor(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo ^ b.lo; r.hi = a.hi ^ b.hi; return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_shl(__c99m_u128 a, int k) {"
    , "  __c99m_u128 r; k = k & 127;"
    , "  if (k == 0) return a;"
    , "  if (k >= 64) { r.hi = a.lo << (k - 64); r.lo = 0; return r; }"
    , "  r.hi = (a.hi << k) | (a.lo >> (64 - k));"
    , "  r.lo = a.lo << k; return r;"
    , "}"
    , "__c99m_u128 __c99m_u128_shr(__c99m_u128 a, int k) {"
    , "  __c99m_u128 r; k = k & 127;"
    , "  if (k == 0) return a;"
    , "  if (k >= 64) { r.lo = a.hi >> (k - 64); r.hi = 0; return r; }"
    , "  r.lo = (a.lo >> k) | (a.hi << (64 - k));"
    , "  r.hi = a.hi >> k; return r;"
    , "}"
    , "static unsigned long long __c99m_umulhi64(unsigned long long a, unsigned long long b) {"
    , "  unsigned long long a0 = a & 0xffffffffULL; unsigned long long a1 = a >> 32;"
    , "  unsigned long long b0 = b & 0xffffffffULL; unsigned long long b1 = b >> 32;"
    , "  unsigned long long t = a0 * b0;"
    , "  unsigned long long k = t >> 32;"
    , "  unsigned long long w1;"
    , "  unsigned long long w2;"
    , "  t = a1 * b0 + k; w1 = t & 0xffffffffULL; w2 = t >> 32;"
    , "  t = a0 * b1 + w1; k = t >> 32;"
    , "  return a1 * b1 + w2 + k;"
    , "}"
    , "__c99m_u128 __c99m_u128_mul(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; r.lo = a.lo * b.lo;"
    , "  r.hi = __c99m_umulhi64(a.lo, b.lo) + a.lo * b.hi + a.hi * b.lo;"
    , "  return r;"
    , "}"
    , "int __c99m_u128_lt(__c99m_u128 a, __c99m_u128 b) {"
    , "  if (a.hi != b.hi) return a.hi < b.hi ? 1 : 0;"
    , "  return a.lo < b.lo ? 1 : 0;"
    , "}"
    , "int __c99m_u128_eq(__c99m_u128 a, __c99m_u128 b) {"
    , "  return (a.lo == b.lo && a.hi == b.hi) ? 1 : 0;"
    , "}"
    , "int __c99m_u128_ne(__c99m_u128 a, __c99m_u128 b) { return !__c99m_u128_eq(a, b); }"
    , "int __c99m_u128_gt(__c99m_u128 a, __c99m_u128 b) { return __c99m_u128_lt(b, a); }"
    , "int __c99m_u128_le(__c99m_u128 a, __c99m_u128 b) { return !__c99m_u128_lt(b, a); }"
    , "int __c99m_u128_ge(__c99m_u128 a, __c99m_u128 b) { return !__c99m_u128_lt(a, b); }"
    , "__c99m_u128 __c99m_u128_divmod(__c99m_u128 n, __c99m_u128 d, __c99m_u128 *rem) {"
    , "  __c99m_u128 q; __c99m_u128 r; int i;"
    , "  q.lo = 0; q.hi = 0; r.lo = 0; r.hi = 0;"
    , "  if (d.lo == 0 && d.hi == 0) { if (rem) *rem = r; return q; }"
    , "  for (i = 127; i >= 0; i--) {"
    , "    unsigned long long bit;"
    , "    if (i >= 64) bit = (n.hi >> (i - 64)) & 1ULL; else bit = (n.lo >> i) & 1ULL;"
    , "    r.hi = (r.hi << 1) | (r.lo >> 63);"
    , "    r.lo = (r.lo << 1) | bit;"
    , "    if (r.hi > d.hi || (r.hi == d.hi && r.lo >= d.lo)) {"
    , "      unsigned long long borrow = (r.lo < d.lo) ? 1ULL : 0ULL;"
    , "      r.lo = r.lo - d.lo;"
    , "      r.hi = r.hi - d.hi - borrow;"
    , "      if (i >= 64) q.hi = q.hi | (1ULL << (i - 64)); else q.lo = q.lo | (1ULL << i);"
    , "    }"
    , "  }"
    , "  if (rem) *rem = r;"
    , "  return q;"
    , "}"
    , "__c99m_u128 __c99m_u128_div(__c99m_u128 a, __c99m_u128 b) {"
    , "  return __c99m_u128_divmod(a, b, (__c99m_u128 *)0);"
    , "}"
    , "__c99m_u128 __c99m_u128_mod(__c99m_u128 a, __c99m_u128 b) {"
    , "  __c99m_u128 r; __c99m_u128_divmod(a, b, &r); return r;"
    , "}"
    ]
